class Monster {
  final String id;
  final String name;
  final String description;
  final String type;
  final String challengeRating;
  final String hitPoints;
  final String armorClass;

  Monster({
    required this.id,
    required this.name,
    required this.description,
    required this.type,
    required this.challengeRating,
    required this.hitPoints,
    required this.armorClass,
  });
}